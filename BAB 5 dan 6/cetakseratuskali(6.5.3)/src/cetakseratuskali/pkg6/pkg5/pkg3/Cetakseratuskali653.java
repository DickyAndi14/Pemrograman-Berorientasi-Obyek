public class Cetakseratuskali653 {
    
    public static void main(String[] args) {
        
        String nama = "DICKY ANDI SOFFAN"; 
        
        // While loop
        int i = 0;
        while (i < 100) {
            System.out.println(nama);
            i++;
        }
        
        // Do while 
        int j = 0;
        do {
            System.out.println(nama);
            j++;
        } while (j < 100);
        
        // For loop
        for (int k = 0; k < 100; k++) {
            System.out.println(nama);
        }
    }
}
